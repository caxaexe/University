/*Написать программу, которая реализует таблицу истинности по формуле ((𝑷 ∨ 𝑸) ∧ ((𝑸 ∨ 𝑹) ∧ (𝑹 ∨ 𝑷))) → ((𝑷 ∧ 𝑸) ∧ 𝑹) */

#include <iostream>
using namespace std;

int main(){

    bool P = 0, Q = 0, R = 0;
    int U = 8;

    cout << " -----------------------------------------------------------------------------------------------------------------------" << endl;
    cout << "| P |" << " Q |" << " R |" << " Q v R |" << " R v P |" << " (Q v R) ∧ (R v P) |" << " P v Q |" << " (P v Q) ∧ (Q v R) ∧ (R v P) |" << " P ∧ Q |" << " (P ∧ Q) ∧ R |" << " F(P,Q,R) |" << endl;
    cout << " -----------------------------------------------------------------------------------------------------------------------" << endl;
    
    while (U--){
        cout << "| " << P << " | " << Q << " | " << R << " |   " << (Q || R) << "   |   ";
        cout << (R || P) << "   |         " << ((Q || R) && (R || P)) << "         |   " ;
        cout << (P || Q) << "   |              " << ((P || Q) && (Q || R) && (R || P)) << "              |   ";
        cout << (P && Q) << "   |      " << ((P && Q) && R) << "      |     " << (((P || Q) && ((Q || R) && (R || P))) == ((P && Q) && R)) << "    |" << endl;
        cout << " -----------------------------------------------------------------------------------------------------------------------" << endl;
    
        if (U > 4) P = 0;
        else P = 1;
        
        if (U > 6 || (U < 5 && U > 2)) Q = 0;
        else Q = 1;

        if (U > 7 || (U < 7 && U > 5) || (U < 5 && U > 3) || (U < 3 && U > 1)) R = 0;
        else R = 1;
        
    }
    return 0;
}
